                  <div class="accrodion-title">
                    <h4><span>Day 2</span> Discover Old &amp; New Delhi</h4>
                  </div>
                  <div class="accrodion-content">
                    <div class="inner">
                      <p style="margin-top: 0.75rem;"><strong>Morning — Old Delhi:</strong> Begin at Jama Masjid, one of India's largest mosques built by Emperor Shah Jahan in 1656. Continue into Chandni Chowk by cycle-rickshaw through the historic spice lanes of Old Delhi. Drive past the imposing facade of the Red Fort (exterior), then stop at Raj Ghat, the serene memorial to Mahatma Gandhi on the banks of the Yamuna.</p>
                      <p style="margin-top: 0.75rem;"><strong>Afternoon — New Delhi:</strong> Visit Humayun's Tomb — the UNESCO World Heritage Site that served as the architectural prototype for the Taj Mahal. Continue to Qutub Minar, the 73-metre UNESCO-listed medieval minaret. End with a panoramic drive past India Gate, Parliament House and Rashtrapati Bhavan — the ceremonial spine of Lutyens' Delhi.</p>
                      <p style="margin-top: 0.75rem;"><strong><strong>Overnight:</strong></strong> Delhi</p>
                    </div>
                  </div>
                </div>

                <div class="accrodion active wow animated fadeInUp" data-wow-delay="0.1s" data-wow-duration="1500ms">
                  <div class="accrodion-title">
                    <h4><span>Day 3</span> Delhi to Agra — Taj Mahal &amp; Agra Fort</h4>
                  </div>
                  <div class="accrodion-content">
                    <div class="inner">
                      <p style="margin-top: 0.75rem;">Morning drive to Agra via the Yamuna Expressway. Hotel check-in and a short break before heading to the Taj Mahal — one of the world's greatest architectural wonders, commissioned by Mughal Emperor Shah Jahan in memory of his wife Mumtaz Mahal and completed in 1653. Your local guide will walk you through the pietra dura inlay work, the optical geometry of the minarets, and the history behind the monument. A sunset visit is especially recommended for the marble's golden-hour glow.</p>
                      <p style="margin-top: 0.75rem;">Continue to Agra Fort — the UNESCO-listed red sandstone citadel housing Jahangiri Mahal, the Khas Mahal and the Musamman Burj, from which Shah Jahan is said to have gazed at the Taj in his final captive years. An optional evening visit to Mehtab Bagh offers a riverfront Taj view at dusk.</p>
                      <p style="margin-top: 0.75rem;"><strong><strong>Overnight:</strong></strong> Agra</p>
                    </div>
                  </div>
                </div>

                <div class="accrodion active wow animated fadeInUp" data-wow-delay="0.1s" data-wow-duration="1500ms">
                  <div class="accrodion-title">
                    <h4><span>Day 4</span> Agra to Ranthambore via Fatehpur Sikri</h4>
                  </div>
                  <div class="accrodion-content">
                    <div class="inner">
                      <p style="margin-top: 0.75rem;">After an early breakfast, depart for Ranthambore with a planned stop at Fatehpur Sikri — Emperor Akbar's ghost capital, abandoned within 14 years of completion in 1585. Explore Buland Darwaza (the largest ceremonial gateway in the world at 54 metres), the Diwan-i-Khas with its distinctive central pillar, the Panch Mahal's five ascending storeys, and the serene Salim Chishti Dargah.</p>
                      <p style="margin-top: 0.75rem;">Continue through the shifting Rajasthan landscape toward Ranthambore. Check in at your wildlife resort or heritage lodge. The evening is at leisure — your naturalist may offer an optional guided nature walk around the property.</p>
                      <p style="margin-top: 0.75rem;"><strong><strong>Overnight:</strong></strong> Ranthambore</p>
                    </div>
                  </div>
                </div>

                <div class="accrodion active wow animated fadeInUp" data-wow-delay="0.1s" data-wow-duration="1500ms">
                  <div class="accrodion-title">
                    <h4><span>Day 5</span> Ranthambore National Park — Into Tiger Country</h4>
                  </div>
                  <div class="accrodion-content">
                    <div class="inner">
                      <p style="margin-top: 0.75rem;">Begin before sunrise as the forest slowly stirs awake. Ranthambore is among India's most photographed tiger reserves, where dry deciduous woodland, lakes, and ruined Mughal-era pavilions create one of the country's most cinematic safari landscapes. Accompanied by an expert naturalist, you'll venture deep into the park in search of Bengal tigers, leopards, sloth bears, striped hyenas, mugger crocodiles, and an extraordinary diversity of bird species.</p>
                      <p style="margin-top: 0.75rem;">Return to your lodge after the morning safari for breakfast and mid-day rest. The afternoon brings a second safari — multiple game drives are the single biggest variable in tiger sighting success, and Ranthambore is one of India's strongest reserves for encounters, particularly between October and June.</p>
                      <p style="margin-top: 0.75rem;">Between safaris, consider visiting Ranthambore Fort — a UNESCO-listed 10th-century hilltop stronghold that overlooks the entire reserve. Unlike Africa's open savannah, Ranthambore offers something distinctly Indian: wildlife framed by ancient stone battlements and centuries of royal hunting history.</p>
                      <p style="margin-top: 0.75rem;"><strong><strong>Overnight:</strong></strong> Ranthambore</p>
                    </div>
                  </div>
                </div>

                <div class="accrodion active wow animated fadeInUp" data-wow-delay="0.1s" data-wow-duration="1500ms">
                  <div class="accrodion-title">
                    <h4><span>Day 6</span> Ranthambore to Jaipur — Into the Pink City</h4>
                  </div>
                  <div class="accrodion-content">
                    <div class="inner">
                      <p style="margin-top: 0.75rem;">Morning departure for Jaipur after breakfast. The scenic drive takes approximately 3 hours through the Rajasthan countryside. Arrive and check in to your hotel. The afternoon can include a visit to Birla Temple or the Albert Hall Museum — Rajasthan's oldest museum, housed in a striking Indo-Saracenic building. The evening is ideal for exploring the old city bazaars: Johari Bazaar for gemstones, Bapu Bazaar for block-print textiles, and the lanes around Hawa Mahal for silver jewellery and lacquerware.</p>
                      <p style="margin-top: 0.75rem;"><strong><strong>Overnight:</strong></strong> Jaipur</p>
                    </div>
                  </div>
