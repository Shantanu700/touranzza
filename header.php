<script>
  (function () {
    var mq = window.matchMedia && window.matchMedia('(min-width: 1200px)');
    if (!mq || !mq.matches) return;

    var li = document.querySelector('.tour-pkg-dropdown');
    if (!li) return;
    var trigger = li.querySelector(':scope > a');
    var menu = li.querySelector(':scope > .mega-menu');
    if (!trigger || !menu) return;

    var closeTimer = null, openTimer = null;

    function openMenu() {
      if (closeTimer) { clearTimeout(closeTimer); closeTimer = null; }
      if (openTimer)  { clearTimeout(openTimer);  openTimer  = null; }
      li.classList.add('is-open');
      trigger.setAttribute('aria-expanded', 'true');
    }
    function closeMenu() {
      if (openTimer) { clearTimeout(openTimer); openTimer = null; }
      li.classList.remove('is-open');
      trigger.setAttribute('aria-expanded', 'false');
    }
    function scheduleOpen()  { if (closeTimer) { clearTimeout(closeTimer); closeTimer = null; } if (openTimer) clearTimeout(openTimer); openTimer  = setTimeout(openMenu,  200); }
    function scheduleClose() { if (closeTimer) clearTimeout(closeTimer); closeTimer = setTimeout(closeMenu, 150); }

    trigger.setAttribute('aria-haspopup', 'true');
    trigger.setAttribute('aria-expanded', 'false');

    trigger.addEventListener('pointerenter', scheduleOpen);
    trigger.addEventListener('pointerleave', scheduleClose);
    menu.addEventListener('pointerenter', openMenu);
    menu.addEventListener('pointerleave', scheduleClose);

    document.addEventListener('keydown', function(e) { if (e.key === 'Escape') closeMenu(); });
    document.addEventListener('click',   function(e) { if (!li.contains(e.target)) closeMenu(); });
  })();
</script>

